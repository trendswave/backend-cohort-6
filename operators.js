//comparison operators 
let x = 10;
let y = 20; 
let z = x == y; // Equal to
let u = x !== y; // Not equal to
let v = x > y; // Greater than
let w = x < y;  // Less than
let m = x >= y; // Greater than or equal to
let n = x <= y; // Less than or equal to            
console.log("Equal to: " + z);
console.log("Not equal to: " + u);
console.log("Greater than: " + v);
console.log("Less than: " + w);
console.log("Greater than or equal to: " + m);
console.log("Less than or equal to: " + n);


